package entiteti;

public class StavkaFakture {
	
	public int FakutraID;
	public int RedniBroj;
	public int UslugaID;
	public int Cena;
	
	public int getFakutraID() {
		return FakutraID;
	}
	public void setFakutraID(int fakutraID) {
		FakutraID = fakutraID;
	}
	public int getRedniBroj() {
		return RedniBroj;
	}
	public void setRedniBroj(int redniBroj) {
		RedniBroj = redniBroj;
	}
	public int getUslugaID() {
		return UslugaID;
	}
	public void setUslugaID(int uslugaID) {
		UslugaID = uslugaID;
	}
	public int getCena() {
		return Cena;
	}
	public void setCena(int cena) {
		Cena = cena;
	}
	
	
	
}
